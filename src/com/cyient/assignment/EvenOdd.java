package com.cyient.assignment;

public class EvenOdd {

	public static void main(String[] args) {
		int n=30;
		for (int i =1; i<=n;i++) 
		   {
	        if (i%2 == 0)
		       {
			     System.out.println("     even numbers" +i);
		       }
		     else
			       System.out.print("      odd numbers" +i);
			      
		       }
			
		   }

}

